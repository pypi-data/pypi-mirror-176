__all__ = ['CommonRotations']


class CommonRotations:
    """ Holds several predefined rotations that may be useful
    
    Attributes:
        Zup_to_Yup: Convert a mesh from Z-up to Y-up
        Yup_to_Zup: Convert a mesh from Y-up to Z-up

    """
    Zup_to_Yup = (-90, 0, 0)
    Yup_to_Zup = (90, 0, 0)
